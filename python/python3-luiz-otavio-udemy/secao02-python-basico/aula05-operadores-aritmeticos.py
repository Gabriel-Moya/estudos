# +, -, *, /, //, **, %, ()

print('10 + 2.5 =', 10 + 2.5)   # Soma
print('10 - 1 =', 10 - 1)       # Subtração
print('5 * 2 =', 5 * 2)         # Multiplicação
print('10 / 3 =', 10 / 3)       # Divisão
print('10 // 3 =', 10 // 3)     # Divisão inteira
print('2 ** 10 =', 2 ** 10)     # Exponenciação
print('10 % 5 =', 10 % 5)       # Resto da divisão
print('10 % 3 =', 10 % 3)       # Resto da divisão

print('Os () tem como objetivo alterar a precedência dos operadores')
print('5 + 2 * 10 =', 5 + 2 * 10)
print('(5 + 2) * 10 =', (5 + 2) * 10)