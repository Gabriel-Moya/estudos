# sep='-'   indica o separador, substituindo o espaço
# end=''    indica o que será impresso no final, e não irá quebrar a linha

print('Gabriel Moya')
print('Gabriel', 'Moya')
print('Gabriel', 'Moya', sep='-')

# Imprimir um CPF com a máscara 123.456.789-01
print('123', '456', '789', sep='.', end='-')
print('01')